num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))

print("A soma de {} com {} é igual a {}".format(num1, num2, num1+num2))
print("A multiplicação de {} por {} é igual a {}".format(num1, num2, num1*num2))
print("A divisão inteira de {} com {} é igual a {}".format(num1, num2, num1//num2))
print("O resto da divisão inteira de {} com {} é igual a {}".format(num1, num2, num1%num2))
